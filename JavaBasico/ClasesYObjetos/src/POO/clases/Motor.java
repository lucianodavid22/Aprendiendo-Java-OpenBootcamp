package POO.clases;

public class Motor {
    // Atributos
    String modeloMotor;
    int caballos;
    double parMm;
    int numCilindros;

    // Constructores
    public Motor(){}

    public Motor(String modeloMotor, int caballos, double parMm, int numCilindros) {
        this.modeloMotor = modeloMotor;
        this.caballos = caballos;
        this.parMm = parMm;
        this.numCilindros = numCilindros;
    }
    // Metodos
}
